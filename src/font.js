/*
 * font.js — a tiny 5x7 bitmap font.
 *
 * Each glyph is 7 bytes, one per row (top -> bottom). Only the low 5 bits of
 * each byte are used; bit 4 (value 16) is the leftmost column, bit 0 the
 * rightmost. A set bit = a lit pixel.
 *
 *   row byte 0b10001  ->  #...#
 *
 * Stored row-major because it is the easiest layout to hand-author and audit.
 * display.js converts these into the SSD1306's column/page memory at draw time.
 *
 * Glyphs cover space, A-Z, a-z, 0-9 and the punctuation the demo needs. Any
 * character without a glyph renders as a hollow box so missing glyphs are
 * visible rather than silently blank.
 */

